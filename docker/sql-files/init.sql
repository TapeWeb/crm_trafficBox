CREATE TABLE IF NOT EXISTS users (
  uID INT AUTO_INCREMENT PRIMARY KEY,
  uToken VARCHAR(255) NOT NULL,
  uName VARCHAR(64) NOT NULL,
  uSurname VARCHAR(64) NOT NULL,
  uPassword VARCHAR(255) NOT NULL,
  uGender VARCHAR(32) NOT NULL,
  uEmail VARCHAR(255) NOT NULL,
  uAge INT NOT NULL CHECK (uAge >= 16),
  uRole VARCHAR(64) DEFAULT 'User',
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS offers (
  oID INT AUTO_INCREMENT PRIMARY KEY,
  oUID INT NOT NULL,
  oName VARCHAR(64) NOT NULL,
  oDescribe VARCHAR(64) DEFAULT NULL,
  oPrice INT NOT NULL,
  oValues INT NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (oUID) REFERENCES users(uID) ON DELETE CASCADE
) ENGINE=InnoDB;
